package OOP.Composition;


public class Wheels {
    private  int wheelsCode;
    private String wheelsName;
    private  double wheelsPrice;
    public String getWheelsName() {
        return wheelsName;
    }

    public void setWheelsName(String wheelsName) {
        this.wheelsName = wheelsName;
    }

    public int getWheelsCode() {
        return wheelsCode;
    }

    public void setWheelsCode(int wheelsCode) {
        this.wheelsCode = wheelsCode;
    }

    public double getWheelsPrice() {
        return wheelsPrice;
    }

    public void setWheelsPrice(double wheelsPrice) {
        this.wheelsPrice = wheelsPrice;
    }





}
